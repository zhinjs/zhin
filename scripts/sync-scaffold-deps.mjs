#!/usr/bin/env node
/**
 * 从 monorepo package.json 同步 create-zhin-app / scaffold-wizard 初始化依赖版本。
 * 在 `pnpm changeset version`（pnpm bump）后运行，保证发版与脚手架 SSOT 一致。
 *
 * Usage:
 *   node scripts/sync-scaffold-deps.mjs          # 写回 stack-versions.generated.json
 *   node scripts/sync-scaffold-deps.mjs --check  # CI：与 workspace 版本比对，不一致则失败
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const outPath = path.join(
  repoRoot,
  'packages/toolkit/scaffold-wizard/src/stack-versions.generated.json',
);

/** npm 包名 → 仓库内 package.json 相对路径 */
const ZHIN_PACKAGE_PATHS = {
  'zhin.js': 'packages/im/zhin/package.json',
  '@zhin.js/cli': 'basic/cli/package.json',
  '@zhin.js/agent': 'packages/im/agent/package.json',
  '@zhin.js/database': 'basic/database/package.json',
  '@zhin.js/host-router': 'packages/host/router/package.json',
  '@zhin.js/host-api': 'packages/host/api/package.json',
  '@zhin.js/client': 'packages/console/client/package.json',
  '@zhin.js/contract': 'packages/console/contract/package.json',
  '@zhin.js/satori': 'packages/toolkit/satori/package.json',
  '@zhin.js/speech': 'packages/toolkit/speech/package.json',
  '@zhin.js/html-renderer': 'packages/toolkit/html-renderer/package.json',
  '@zhin.js/mcp': 'packages/host/mcp/package.json',
  '@zhin.js/adapter-sandbox': 'plugins/adapters/sandbox/package.json',
};

const AI_PEER_SOURCE = 'packages/im/ai/package.json';
const AGENT_PEER_SOURCE = 'packages/im/agent/package.json';

const AI_STACK_PEER_KEYS = [
  'ai',
  'zod',
  '@ai-sdk/openai',
  '@ai-sdk/anthropic',
  '@ai-sdk/google',
  '@ai-sdk/deepseek',
  '@ai-sdk/openai-compatible',
];

/** @param {string} relPath */
function readPackageJson(relPath) {
  const abs = path.join(repoRoot, relPath);
  if (!fs.existsSync(abs)) {
    throw new Error(`sync-scaffold-deps: missing ${relPath}`);
  }
  return JSON.parse(fs.readFileSync(abs, 'utf8'));
}

/** @param {string} version */
function caretFromVersion(version) {
  const match = String(version).match(/^(\d+)\.(\d+)\.(\d+)/);
  if (!match) return `^${version}`;
  return `^${match[1]}.${match[2]}.${match[3]}`;
}

function discoverAdapterPackages() {
  /** @type {Record<string, string>} */
  const adapters = {};
  const adaptersDir = path.join(repoRoot, 'plugins/adapters');
  if (!fs.existsSync(adaptersDir)) return adapters;
  for (const entry of fs.readdirSync(adaptersDir, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue;
    const pkgPath = path.join(adaptersDir, entry.name, 'package.json');
    if (!fs.existsSync(pkgPath)) continue;
    const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf8'));
    if (pkg.name?.startsWith('@zhin.js/adapter-')) {
      adapters[pkg.name] = path.relative(repoRoot, pkgPath);
    }
  }
  return adapters;
}

function buildStackVersions() {
  /** @type {Record<string, string>} */
  const zhinStack = {};
  const packagePaths = {
    ...ZHIN_PACKAGE_PATHS,
    ...discoverAdapterPackages(),
  };
  for (const [name, relPath] of Object.entries(packagePaths)) {
    const pkg = readPackageJson(relPath);
    if (!pkg.version) {
      throw new Error(`sync-scaffold-deps: ${relPath} has no version`);
    }
    zhinStack[name] = caretFromVersion(pkg.version);
  }

  const aiPkg = readPackageJson(AI_PEER_SOURCE);
  const agentPkg = readPackageJson(AGENT_PEER_SOURCE);
  const aiPeers = aiPkg.peerDependencies ?? {};
  const agentPeers = agentPkg.peerDependencies ?? {};

  /** @type {Record<string, string>} */
  const aiStack = {
    '@zhin.js/agent': caretFromVersion(agentPkg.version),
  };

  for (const key of AI_STACK_PEER_KEYS) {
    const range = aiPeers[key];
    if (!range) {
      throw new Error(`sync-scaffold-deps: ${AI_PEER_SOURCE} missing peerDependencies.${key}`);
    }
    aiStack[key] = range;
  }

  const mcpRange = agentPeers['@modelcontextprotocol/sdk'];
  if (!mcpRange) {
    throw new Error(`sync-scaffold-deps: ${AGENT_PEER_SOURCE} missing peer @modelcontextprotocol/sdk`);
  }
  aiStack['@modelcontextprotocol/sdk'] = mcpRange;

  return {
    $comment: 'Auto-generated by scripts/sync-scaffold-deps.mjs — do not edit; run pnpm sync:scaffold-deps',
    zhinStack,
    aiStack,
  };
}

/** @param {unknown} value */
function stableStringify(value) {
  return `${JSON.stringify(value, null, 2)}\n`;
}

function main() {
  const check = process.argv.includes('--check');
  const next = buildStackVersions();
  const nextText = stableStringify(next);

  if (check) {
    if (!fs.existsSync(outPath)) {
      console.error('sync-scaffold-deps --check: missing stack-versions.generated.json — run pnpm sync:scaffold-deps');
      process.exit(1);
    }
    const current = fs.readFileSync(outPath, 'utf8');
    if (current !== nextText) {
      console.error('sync-scaffold-deps --check: stack-versions.generated.json is out of date.');
      console.error('  Run: pnpm sync:scaffold-deps');
      console.error(`  Output: ${path.relative(repoRoot, outPath)}`);
      process.exit(1);
    }
    console.log('Scaffold deps SSOT check passed (stack-versions.generated.json ↔ workspace package.json).');
    return;
  }

  fs.writeFileSync(outPath, nextText);
  console.log(`Updated ${path.relative(repoRoot, outPath)}`);
  for (const [name, range] of Object.entries(next.zhinStack)) {
    console.log(`  zhin  ${name}@${range}`);
  }
  for (const [name, range] of Object.entries(next.aiStack)) {
    console.log(`  ai    ${name}@${range}`);
  }
}

main();
